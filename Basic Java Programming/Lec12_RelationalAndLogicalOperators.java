
public class Lec12_RelationalAndLogicalOperators {
    public static void main(String[] args) {
        System.out.println("Relational and logical operators in java programming");
        System.out.println("For logical AND (&&) operators");
        boolean a = true;
        boolean b = false;
        if (a && b) {
            System.out.println("Y");
        }

        else {
            System.out.println("N");
        }

        System.out.println("For logical OR(||)operators");
        if (a || b) {
            System.out.println("Y");
        }

        else {
            System.out.println("N");
        }

        System.out.println("for not operator(!) java programming");
        System.out.print("NOT a is : ");
        System.out.println(!(a));
        System.out.print("NOT b is: ");
        System.out.println(!(b));

    }
}
