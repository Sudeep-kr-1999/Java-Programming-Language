public class Lec04_LiteralsInJava {
    public static void main(String[] args) {
        System.out.println("Literals in java programming");
        byte age=34;
        char ch='a';
        float f1=5.1f;    // small 'f' or capital 'F'both are allowed!
        double d1=5.6d;  //capital 'D' or small 'd' both are allowed.!
        // note:-------->decimal is by default double in type.........!

        long l1=5L;    // for long capital "L" is used...!
        boolean a=true;
        String name="Sudeep";
        System.out.println(name);
        System.out.println(a);
        System.out.println(l1);
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);


    }
}
